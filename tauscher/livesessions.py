import numpy as np

# braodcasting example
a = np.array([1,2,3])
b = np.array([4,5,6])
c = a * b

print(c)

# 2D array

a = np.array([[1,2,3],[4,5,6]])
b = np.array([[1,2,3],[4,5,6]])
c = a * b

print(c)

# 3D array

a = np.array([[[1,2,3],[4,5,6]],[[1,2,3],[4,5,6]]])
b = np.array([[[1,2,3],[4,5,6]],[[1,2,3],[4,5,6]]])
c = a * b

print(c)



